public class Tsk_012 {
    public static void main(String[] args) {

        char ch ='a';

        if(ch >= 'A' && ch <= 'Z' || ch >= 'a' && ch <= 'z' ){
            System.out.println("Alphabet");
        } else {
            System.out.println("Not an alphabet");
        }
    }
}
