package ex_10_switch;

public class Lab079_SwitchIQ7 {
    public static void main(String[] args) {

        char code ='C';

        switch (code){
            default:
                System.out.println("Hellooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
