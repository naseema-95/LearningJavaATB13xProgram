package ex_03_Java_Literals;

public class Lab019_Literals_1 {
    public static void main(String[] arg) {
        int age=65;
        System.out.printf("integer literal is %d\n",age);

        float fl=2.43f;
        System.out.printf("Float literal is %f\n",fl);

        final int bl=67;
        System.out.printf("final iteger literal constant is %d\n",bl);

        boolean b=true;
        boolean a=false;
        System.out.println(b);
        System.out.println(a);

        int Enum=9; //enum is keyword;
        System.out.println(Enum);

    }
}
